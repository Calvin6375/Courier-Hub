    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String current = jCpassword.getText();
    String newpass = jNpassword.getText();
    String conpass = jCNpassword.getText();
    
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        sqlCon = DriverManager.getConnection(database, username, password);
        
        // Check if the current password matches
        PreparedStatement pstCheck = sqlCon.prepareStatement("SELECT * FROM user WHERE Password = ?");
        pstCheck.setString(1, current);
        ResultSet rs = pstCheck.executeQuery();
        if (rs.next()) {
            // Current password is correct, proceed with update
            pst = sqlCon.prepareStatement("UPDATE user SET Password = ? WHERE Password = ?");
            pst.setString(1, conpass); // Set the new password
            pst.setString(2, current); // Set the current password in WHERE clause
            
            // Execute the update query
            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Password updated successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to update password!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Current password is incorrect!");
        }

        // Close resources
        rs.close();
        pstCheck.close();
        pst.close();
        sqlCon.close();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

