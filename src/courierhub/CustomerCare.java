    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String message = jMessage.getText(); // Replace this with actual text area content
        
        try {
            // Establish database connection
            Connection connection = DriverManager.getConnection(database, username, password);
            
            // SQL query to insert data into table
            String query = "INSERT INTO customercare (Message) VALUES (?)"; // Replace with your table and column names
            
            // Create a prepared statement
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            
            // Set the parameter value
            preparedStatement.setString(1, message);
            
            // Execute the query
            int rowsAffected = preparedStatement.executeUpdate();
            
            if (rowsAffected > 0) {
                
                JOptionPane.showMessageDialog(null, "Message sent successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Message not sent");
            }
            
            // Close resources
            preparedStatement.close();
            connection.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    
    }//GEN-LAST:event_jButton2ActionPerformed

