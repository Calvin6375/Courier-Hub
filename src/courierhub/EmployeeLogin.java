    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String un = jId.getText();
        String ps = jPass.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/courier","root","");
            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("select * from employee where EmployeeId='"+un+"'and Password='"+ps+"'");
            
            if (rs.next()) {
               EmployeeHome eh = new EmployeeHome();
               eh.jLabel3.setText(rs.getString("EmployeeName"));
               eh.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect details");
            }
            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }               
            dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

