    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String un = jUname.getText();
        String ps = jPass.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/courier","root","");
            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("select * from user where UserName='"+un+"'and Password='"+ps+"'");
            
            if (rs.next()) {
               Client c = new Client();
               c.jCname.setText(rs.getString("UserName"));
               c.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect details");
            }
            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }               
            dispose();
    }//GEN-LAST:event_jButton5ActionPerformed

