    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            String FName=jFname.getText();
            String LName=jLname.getText();
            String UName=jUname.getText();
            String Pass=jPassword.getText();
            String Address=jPaddress.getText();
            String County=jCounty.getText();
            String Email=jEmail.getText();
            String Number=jPnumber.getText();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/courier","root","");
            PreparedStatement ps=con.prepareStatement("insert into user(FirstName,LastName,UserName,Password,PostalAddress,CountyOfResidence,Email,PhoneNumber)values(?,?,?,?,?,?,?,?)");
            
            ps.setString(1,FName);
            ps.setString(2,LName);
            ps.setString(3,UName);
            ps.setString(4,Pass);
            ps.setString(5,Address);
            ps.setString(6,County);
            ps.setString(7,Email);
            ps.setString(8,Number);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Insertion successful");
            
            Login l = new Login();
            l.setVisible(true);
            
            dispose();
            
        } catch (Exception ex){
           JOptionPane.showMessageDialog(this, "Insertion not successful");
        }   
        
    }//GEN-LAST:event_jButton2ActionPerformed

